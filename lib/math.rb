def addition(num1, num2)
  add = num1 + num2
  add
end

def subtraction(num1, num2)
  subtr = num2 - num1
  subtr
end

def division(num1, num2)
  div = (num2)/(num1)
  div
end

def multiplication(num1, num2)
  mult = num1 * num2
  mult
end

def modulo(num1, num2)
  mod = num2 % num1
  mod
end

def square_root(num)
  squar = Math.sqrt(num)
  squar
end